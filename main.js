// ES6 Syntax
// Biến let, const, var
var a = 10;
var a = 20;
console.log(a);
// var cho phép thay đổi đè lên

let b = 10;
b = 20;
console.log(b);
// let không cho phép khai báo lại đè lên

const c = 10;
console.log(c);
// const là duy nhất không thay đổi

// Khi nào sử dụng const(mặc định dùng), var(không nên dùng), let(khi xác định có giá trị thay đổi)

